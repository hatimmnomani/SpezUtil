#!/usr/bin/env node
// Regenerates the embedded Arabic-font module in every package that ships one.
// Run: node scripts/generate-font-asset.mjs
//
// To swap fonts (e.g. once written permission is obtained for a different
// font), this is a config change, not a code change:
//   1. Drop the new TTF — and its license file, if any — into assets/fonts/.
//   2. Point the relevant target's `font` at the new { file, family }.
//   3. Re-run this script.
// Every consuming package imports the same export names
// (ARABIC_FONT_FAMILY / arabicFontDataUrl / arabicFontFace) regardless of
// which font is embedded, so a swap never requires touching a package's
// styles.ts — only this config and the font asset itself.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));

/** Currently embedded font. SIL OFL-1.1 — see assets/fonts/OFL-Amiri.txt. */
const AMIRI = {
  file: "assets/fonts/Amiri-Regular.ttf",
  family: "Amiri",
};

/**
 * One entry per package that embeds an Arabic font. `module` is the
 * generated file path; `font` is the { file, family } source for that
 * target (kept per-target, not global, so a single package could embed a
 * different font in future without restructuring this script).
 */
const targets = [
  { module: "packages/hijri-calendar/src/font-arabic.ts", font: AMIRI },
  { module: "packages/hijri-datepicker/src/font-arabic.ts", font: AMIRI },
  { module: "packages/richtext-editor/src/font-arabic.ts", font: AMIRI },
];

const base64Cache = new Map();
function base64For(relFontPath) {
  let b64 = base64Cache.get(relFontPath);
  if (b64 === undefined) {
    b64 = readFileSync(path.join(root, relFontPath)).toString("base64");
    base64Cache.set(relFontPath, b64);
  }
  return b64;
}

for (const { module, font } of targets) {
  const base64 = base64For(font.file);
  const banner = `// AUTO-GENERATED by scripts/generate-font-asset.mjs — do not edit by hand.\n// Source: ${font.file}\n`;

  const content = `${banner}
export const ARABIC_FONT_FAMILY = "${font.family}";

// ": string" keeps TypeScript from inlining the ~500 KB literal into every
// consumer's .d.ts — without it, TS inlines the literal type instead of
// widening to \`string\`.
export const arabicFontDataUrl: string = "data:font/ttf;base64,${base64}";

export const arabicFontFace: string = \`@font-face {
  font-family: "\${ARABIC_FONT_FAMILY}";
  src: url(\${arabicFontDataUrl}) format("truetype");
  font-weight: normal;
  font-style: normal;
  font-display: swap;
}\`;
`;

  writeFileSync(path.join(root, module), content);
  console.log(`wrote ${module} (${(content.length / 1024).toFixed(1)} kB) — family "${font.family}"`);
}
