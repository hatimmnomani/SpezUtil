#!/usr/bin/env node
// Regenerates the embedded Al-Kanz font module in every package that ships it.
// Run: node scripts/generate-font-asset.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const fontPath = path.join(root, "assets/fonts/Al-Kanz.ttf");
const base64 = readFileSync(fontPath).toString("base64");

const targets = [
  "packages/hijri-calendar/src/font-al-kanz.ts",
  "packages/hijri-datepicker/src/font-al-kanz.ts",
];

const banner = "// AUTO-GENERATED by scripts/generate-font-asset.mjs — do not edit by hand.\n// Source: assets/fonts/Al-Kanz.ttf\n";

const content = `${banner}
export const ALKANZ_FONT_FAMILY = "Al-Kanz";

export const alKanzFontFace = \`@font-face {
  font-family: "\${ALKANZ_FONT_FAMILY}";
  src: url(data:font/ttf;base64,${base64}) format("truetype");
  font-weight: normal;
  font-style: normal;
  font-display: swap;
}\`;
`;

for (const rel of targets) {
  writeFileSync(path.join(root, rel), content);
  console.log(`wrote ${rel} (${(content.length / 1024).toFixed(1)} kB)`);
}
